#!/usr/bin/env python3
"""
sria_rt_v051_deployment_output_separation.py

SRIA RT v0.5.1 - Deployment-Style Output Separation

Purpose:
- Apply an exported learned SRIA RT ranker to accepted episode JSONL.
- Produce clean deployment-facing analyst queues with no redteam/label fields.
- Optionally produce research/debug queues with validation fields preserved.
- Preserve SRIA explanation metadata while separating it from validation artifacts.

This script does NOT rescan auth.txt and does NOT train a model.
It only loads an existing model artifact generated by prior SRIA RT comparison/export scripts.

Typical use from F:\SRIA\SRIA_RT_v01:

  py sria_rt_v051_deployment_output_separation.py ^
    --accepted-jsonl v036_batches\episodes_v036_accepted.jsonl ^
    --model-artifact v050_train_v033_score_v036\model_v033_rf_depth10_cw_none.joblib ^
    --out-dir v051_deployment_rf_depth10 ^
    --queue-sizes 100,500,1000,5000 ^
    --write-research-debug ^
    --matches-jsonl v036_batches\redteam_matches_v036_FINAL_SPARSE.jsonl

Deployment outputs intentionally exclude:
- label
- redteam_count
- redteam_indices
- exact_start_count
- redteam_group
- any direct validation-only fields

Research/debug outputs may include those fields when --write-research-debug is supplied.
"""

